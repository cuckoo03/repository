package com.service.hibernate.manytomany2;

import java.util.HashSet;
import java.util.Set;

public class Item {
	private Integer id;
	private Set categoris = new HashSet();

	public Integer getId() {
		return id;
	}

	private void setId(Integer id) {
		this.id = id;
	}

	public Set getCategoris() {
		return categoris;
	}

	private void setCategoris(Set categoris) {
		this.categoris = categoris;
	}
}
